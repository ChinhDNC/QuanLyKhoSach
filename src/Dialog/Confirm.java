    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        TaiKhoan x = new DAO().getTK(parent.lblusername.getText());
        if (txtpass.getText().equals(x.getPass())) {
            TaiKhoan tk = new TaiKhoan();
            tk.setUser(parent.lblusername.getText());
            if(parent.txtpass.getText().equals(""))
                tk.setPass(txtpass.getText());
            else tk.setPass(parent.txtpass.getText());
            tk.setRole(parent.lblrole.getText());
            tk.setStatus("Hoạt động");
            tk.setPhone(parent.txtphone.getText());
            int res = JOptionPane.showConfirmDialog(rootPane, "Lưu chỉnh sửa?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                if (new DAO().editTK(tk)) {
                    parent.txtpass.setText("");
                    parent.txtphone.setText(tk.getPhone());
                    this.dispose();
                    JOptionPane.showMessageDialog(this, "Chỉnh sửa thông tin thành công");
                } else {
                    this.dispose();
                    JOptionPane.showMessageDialog(this, "Chỉnh sửa thất bại");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Chỉnh sửa chưa được lưu lại");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sai mật khẩu" );
        }
    }//GEN-LAST:event_jButton1MouseClicked

